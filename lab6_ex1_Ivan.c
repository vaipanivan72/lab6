#include <stdio.h>

int main() {
    int choice;

    do {
        // Вивід меню
        printf("\n========== МЕНЮ ==========\n");
        printf("1. Ввести матрицю та замінити 2 найбільші елементи нулями\n");
        printf("0. Вихід з програми\n");
        printf("==========================\n");
        printf("Ваш вибір: ");
        scanf("%d", &choice);

        switch (choice) {
            case 1: {
                int n, m;
                printf("Введіть кількість рядків n: ");
                scanf("%d", &n);
                printf("Введіть кількість стовпців m: ");
                scanf("%d", &m);

                // Перевірка, щоб масив мав хоча б 2 елементи
                if (n * m < 2) {
                    printf("Помилка: Матриця повинна містити хоча б 2 елементи!\n");
                    break;
                }

                // Оголошення двовимірного масиву [cite: 316]
                int A[n][m];

                // Введення елементів матриці з клавіатури [cite: 348]
                printf("Введіть елементи матриці:\n");
                for (int i = 0; i < n; i++) {
                    for (int j = 0; j < m; j++) {
                        printf("A[%d][%d] = ", i, j);
                        scanf("%d", &A[i][j]);
                    }
                }

                // Вивід початкової матриці
                printf("\nПочаткова матриця:\n");
                for (int i = 0; i < n; i++) {
                    for (int j = 0; j < m; j++) {
                        printf("%d\t", A[i][j]);
                    }
                    printf("\n");
                }

                // Крок 1: Пошук координат першого найбільшого елемента
                int max1_i = 0, max1_j = 0;
                for (int i = 0; i < n; i++) {
                    for (int j = 0; j < m; j++) {
                        if (A[i][j] > A[max1_i][max1_j]) {
                            max1_i = i;
                            max1_j = j;
                        }
                    }
                }

                // Крок 2: Пошук координат другого найбільшого елемента (пропускаючи перший)
                int max2_i = -1, max2_j = -1;
                for (int i = 0; i < n; i++) {
                    for (int j = 0; j < m; j++) {
                        // Пропускаємо координати першого знайденого максимуму
                        if (i == max1_i && j == max1_j) continue; 
                        
                        // Записуємо перший-ліпший допустимий елемент або шукаємо більший
                        if (max2_i == -1 || A[i][j] > A[max2_i][max2_j]) {
                            max2_i = i;
                            max2_j = j;
                        }
                    }
                }

                // Крок 3: Заміна знайдених максимумів на 0
                A[max1_i][max1_j] = 0;
                if (max2_i != -1) {
                    A[max2_i][max2_j] = 0;
                }

                // Вивід результуючої матриці
                printf("\nМатриця після заміни 2 найбільших елементів нулями:\n");
                for (int i = 0; i < n; i++) {
                    for (int j = 0; j < m; j++) {
                        printf("%d\t", A[i][j]);
                    }
                    printf("\n");
                }
                break;
            }

            case 0:
                printf("Роботу завершено. До побачення!\n");
                break;

            default:
                printf("Помилка: Невірний пункт меню. Спробуйте ще раз.\n");
                break;
        }

    } while (choice != 0);

    return 0;
}

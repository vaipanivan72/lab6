#include <stdio.h>
#include <stdlib.h>
#include <time.h>

int main() {
    int choice;
    
    // Ініціалізація генератора випадкових чисел
    srand(time(0)); // [cite: 119]

    do {
        // Вивід меню
        printf("\n========== МЕНЮ ==========\n");
        printf("1. Згенерувати матрицю та знайти максимум вище головної діагоналі\n");
        printf("0. Вихід з програми\n");
        printf("==========================\n");
        printf("Ваш вибір: ");
        scanf("%d", &choice);

        switch (choice) {
            case 1: {
                int n;
                printf("Введіть розмір квадратної матриці n: ");
                scanf("%d", &n);

                // Якщо матриця 1x1, то над діагоналлю немає елементів
                if (n < 2) {
                    printf("Помилка: Для пошуку елементів вище головної діагоналі розмір матриці має бути щонайменше 2x2.\n");
                    break;
                }

                // Оголошення двовимірного масиву дійсних чисел (VLA)
                double A[n][n];

                // Генерація та вивід матриці
                printf("\nЗгенерована матриця дійсних чисел [-100; 100]:\n");
                for (int i = 0; i < n; i++) { // [cite: 120]
                    for (int j = 0; j < n; j++) { // [cite: 122]
                        // Генеруємо випадкове дійсне число від -100.0 до 100.0
                        A[i][j] = ((double)rand() / RAND_MAX) * 200.0 - 100.0;
                        
                        // Виводимо з точністю до 2 знаків після коми
                        printf("%8.2f ", A[i][j]); 
                    }
                    printf("\n"); // [cite: 128]
                }

                // Пошук максимального елемента вище головної діагоналі
                // Всі елементи вище головної діагоналі мають умову індексів: j > i
                
                // Беремо за початковий максимум перший-ліпший елемент вище діагоналі (A[0][1])
                double max = A[0][1]; 
                int max_i = 0, max_j = 1;

                for (int i = 0; i < n; i++) {
                    // Починаємо внутрішній цикл від i + 1, щоб перевіряти ТІЛЬКИ те, що вище діагоналі
                    for (int j = i + 1; j < n; j++) { 
                        if (A[i][j] > max) { // [cite: 135]
                            max = A[i][j]; // [cite: 136]
                            max_i = i;
                            max_j = j;
                        }
                    }
                }

                // Вивід результату
                printf("\n--- Результат ---\n");
                printf("Максимальний елемент вище головної діагоналі: %.2f\n", max);
                printf("Він знаходиться на позиції: рядок %d, стовпець %d (A[%d][%d])\n", max_i, max_j, max_i, max_j);
                break;
            }

            case 0:
                printf("Роботу завершено. До побачення!\n");
                break;

            default:
                printf("Помилка: Невірний пункт меню. Спробуйте ще раз.\n");
                break;
        }

    } while (choice != 0);

    return 0; 
}
